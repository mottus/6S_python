def solirr(wl):
    """
    Solar spectral irradiance at wavelength wl (active spectrum: TSIS-1 HSRS).

    Uses _SI, which defaults to _SI_TSIS1 (Coddington et al. 2021).
    To use the original Wehrli (1985) spectrum, set _SI = _SI_WEHRLI
    before calling this function.

    Parameters
    ----------
    wl  : float — wavelength in microns (0.25–4.0)

    Returns
    -------
    float — solar spectral irradiance in W m-2 um-1 at 1 AU
    """
    pas = 0.0025
    iwl = int((wl - 0.250) / pas + 1.5)   # 1-based index
    if iwl < 1:
        print("      wavelength less than 0.25 micron:\n"
              "let's take s(l)=s(0.25)", file=sys.stderr)
        return _SI[0]
    if iwl > len(_SI):
        return _SI[-1]
    return _SI[iwl - 1]


def solirr_wehrli(wl):
    """
    Solar spectral irradiance from the original Wehrli (1985) spectrum.

    This is the spectrum used in 6S since its original Fortran implementation
    (Vermote et al. 1997).  Kept here so it can be called directly to
    quantify the difference between Wehrli and the updated TSIS-1 spectrum:

        diff_pct = (solirr_wehrli(wl) - solirr(wl)) / solirr(wl) * 100

    Typical differences vs TSIS-1:
        400 nm:  +1.8%    550 nm:  +0.9%    700 nm:  +1.3%
        1000 nm: +1.9%   1600 nm:  +4.7%   2200 nm:  +7.8%

    Parameters
    ----------
    wl  : float — wavelength in microns (0.25–4.0)

    Returns
    -------
    float — solar spectral irradiance in W m-2 um-1 at 1 AU (Wehrli 1985)
    """
    pas = 0.0025
    iwl = int((wl - 0.250) / pas + 1.5)
    if iwl < 1:
        return _SI_WEHRLI[0]
    if iwl > len(_SI_WEHRLI):
        return _SI_WEHRLI[-1]
    return _SI_WEHRLI[iwl - 1]