def _fintexp1(xtau):
    """Exponential integral E1 approximation (accuracy ~2e-7 for 0 < xtau < 1)."""
    if xtau <= 0.0:
        return 0.0
    a = [-0.57721566, 0.99999193, -0.24991055,
          0.05519968, -0.00976004,  0.00107857]
    xx    = a[0]
    xftau = 1.0
    for i in range(1, 6):
        xftau *= xtau
        xx    += a[i] * xftau
    return xx - math.log(xtau)


def _fintexp3(xtau):
    """Third exponential integral approximation."""
    return (math.exp(-xtau) * (1.0 - xtau) + xtau**2 * _fintexp1(xtau)) / 2.0


def csalbr(xtau):
    """
    Spherical albedo of a Rayleigh atmosphere.

    Parameters
    ----------
    xtau : float – Rayleigh optical thickness

    Returns
    -------
    xalb : float – spherical albedo
    """
    xalb = (3.0 * xtau - _fintexp3(xtau) * (4.0 + 2.0 * xtau) +
            2.0 * math.exp(-xtau))
    xalb /= (4.0 + 3.0 * xtau)
    return xalb


