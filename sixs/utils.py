# VARSOL  (VARSOL.f)
# ---------------------------------------------------------------------------

def varsol(jday, month):
    """
    Variability of the solar constant during the year.

    Parameters
    ----------
    jday  : int  – day of the month
    month : int  – month number (1-12)

    Returns
    -------
    dsol : float – multiplicative factor for the mean solar constant
    """
    if month <= 2:
        j = 31 * (month - 1) + jday
    elif month > 8:
        j = 31 * (month - 1) - ((month - 2) // 2) - 2 + jday
    else:
        j = 31 * (month - 1) - ((month - 1) // 2) - 2 + jday

    pi = 2.0 * math.acos(0.0)
    om = (0.9856 * float(j - 4)) * pi / 180.0
    dsol = 1.0 / ((1.0 - 0.01673 * math.cos(om)) ** 2)
    return dsol


# ---------------------------------------------------------------------------
# ODRAYL  (ODRAYL.f)
# ---------------------------------------------------------------------------

def odrayl(wl):
    """
    Molecular (Rayleigh) optical depth.

    Uses global commons: atm (z, p, t), delta_sigma (delta, sigma).

    Parameters
    ----------
    wl : float – wavelength in microns

    Returns
    -------
    tray : float – Rayleigh optical depth
    """
    z  = atm.z
    p  = atm.p
    t  = atm.t
    delta = delta_sigma.delta

    pi  = 3.1415926
    ak  = 1.0 / wl
    awl = float(wl)

    # Air refraction index (Edlen 1966, Metrologia 2, 71-80, pw=0)
    a1 = 130.0 - ak * ak
    a2 = 38.9  - ak * ak
    a3 = 2406030.0 / a1
    a4 = 15997.0   / a2
    an = (8342.13 + a3 + a4) * 1.0e-8
    an = an + 1.0

    d2 = 2.0 + delta
    a  = (24.0 * pi**3) * ((an**2 - 1.0)**2) * (6.0 + 3.0 * delta) / \
         (6.0 - 7.0 * delta) / ((an**2 + 2.0)**2)

    tray = 0.0
    ns   = 2.54743e19
    for k in range(33):          # k = 0..32  →  Fortran k=1..33
        dppt = (288.15 / 1013.25) * (p[k] / t[k] + p[k+1] / t[k+1]) / 2.0
        sr   = (a * dppt / (awl**4) / ns * 1.0e16) * 1.0e5
        tray += (z[k+1] - z[k]) * sr

    return tray


